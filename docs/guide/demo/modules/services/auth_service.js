
app.factory('authService',function(){

//------------------------------------------------ Variablen -------------------------------------------------------------------------------------------------------------------------------------//
    var service = {};

//------------------------------------------------ Funktionen --------------------------------------------------------------------------------------------------------------------------------------//
    var isLoggedIn = false;

    service.isLoggedIn = function () {
        return  isLoggedIn;
    };

    service.setLogin = function (bool) {
        isLoggedIn = bool;
    };

    return service;
});

